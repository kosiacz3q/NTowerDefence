#include "TexturedCube.h"


TexturedCube::TexturedCube(ShaderPtr shader, TexturePtr texture)
	:shader(shader), texture(texture)
{

}

TexturedCube::~TexturedCube()
{
	glDeleteBuffers(1, &vertexBufferId);
	glDeleteBuffers(1, &uvBufferId);

	glDeleteVertexArrays(1, &vertexArrayId);

	delete[] vertexBufferData;
	delete[] uvBufferData;
}

void TexturedCube::draw(DrawingContextPtr drawingContext)
{
	auto model = glm::mat4(1.0f);
	glm::mat4  MVP = drawingContext->projection * drawingContext->view * model; // Remember, matrix multiplication is the other way around

	glUseProgram(shader->getId());
	// Send our transformation to the currently bound shader,
	// in the "MVP" uniform
	// For each model you render, since the MVP will be different (at least the M part)
	glUniformMatrix4fv(matrixID, 1, GL_FALSE, &MVP[0][0]);

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, texture->Id);
	// Set our "myTextureSampler" sampler to user Texture Unit 0
	glUniform1i(textureSamplerId, 0);

	// 1rst attribute buffer : vertices
	glEnableVertexAttribArray(0);
	glBindBuffer(GL_ARRAY_BUFFER, vertexBufferId);
	glVertexAttribPointer(
		0,                  // attribute. No particular reason for 0, but must match the layout in the shader.
		3,                  // size
		GL_FLOAT,           // type
		GL_FALSE,           // normalized?
		0,                  // stride
		(void*)0            // array buffer offset
		);

	// 2nd attribute buffer : UVs
	glEnableVertexAttribArray(1);
	glBindBuffer(GL_ARRAY_BUFFER, uvBufferId);
	glVertexAttribPointer(
		1,                                // attribute. No particular reason for 1, but must match the layout in the shader.
		2,                                // size : U+V => 2
		GL_FLOAT,                         // type
		GL_FALSE,                         // normalized?
		0,                                // stride
		(void*)0                          // array buffer offset
		);

	// Draw the triangle !
	glDrawArrays(GL_TRIANGLES, 0, 12 * 3); // 12*3 indices starting at 0 -> 12 triangles

	glDisableVertexAttribArray(0);
	glDisableVertexAttribArray(1);
}

